    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.jLabel4.setVisible(false);                                         // Creo MexAppletClient
        String str1 = jTextField1.getText();
        String str2 = jTextField2.getText();
        String str3 = jTextField3.getText();
        
        if(!str3.isEmpty() && !str2.isEmpty() || !str3.isEmpty())
            this.jLabel4.setVisible(true);
        else
            if(str1.isEmpty() && str2.isEmpty() && str3.isEmpty())
                mAC = new MexAppletClient("localhost","Anonms",4444);
            else
                if(!str1.isEmpty() && str2.isEmpty() && str3.isEmpty())
                    mAC = new MexAppletClient("localhost",str1,4444);
                else
                    if(!str1.isEmpty() && !str2.isEmpty() && !str3.isEmpty())
                        mAC = new MexAppletClient(str2,str1,Integer.valueOf(str3));
        this.setContentPane(jPanel2);                                           // Cambio Pannello
        this.jToggleButton1.setText("On");
        this.jLabel8.setVisible(false);
        this.setSize(400,650);    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        mAC.askMexBoard();                                                      // Recupero Messaggi
        ArrayList<String> MexBoard = mAC.getMexBoard();
        jTextArea1.setText("Username\t|\tMex\t\n\n");
        for(String str : MexBoard){
            int i=0;
            for(;str.charAt(i)!='|';i++){}
            if(str.substring(0,i).length()<=12)
                jTextArea1.append(MexBoard.indexOf(str)+"] "+str.substring(0,i)+"\t| "+str.substring((i+1),str.length())+"\n");
            else
                jTextArea1.append(MexBoard.indexOf(str)+"] "+str.substring(0,i)+" | "+str.substring((i+1),str.length())+"\n");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jLabel8.setVisible(false);                                              // Invio Messaggio
        String str = jTextField4.getText();
        if(str.isEmpty())
            jLabel8.setVisible(true);
        else{
            mAC.sendMex(str);
            jTextArea1.append(mAC.getUsername()+(mAC.getUsername().length()<=12?"\t| ":" | ")+str+"\n");
        }    
            
                    
    }//GEN-LAST:event_jButton2ActionPerformed

